import java.lang.Math;
public class Main {
    public static void main(String[] args)
    {
        double a = 30;
        double b = 2;
        double d = 145.256;
        double c = 1.0;
        float x = 4567.9874f;
        System.out.println(Math.pow(a,b));  // 900.0
        System.out.println(Math.log(d));  // 4.978497702968366
        System.out.println(Math.round(x));  // 4568
        System.out.println(Math.asin(c));  // 1.5707963267948966
    }
}
